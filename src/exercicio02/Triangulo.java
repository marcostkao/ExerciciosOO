package exercicio02;

public class Triangulo {
    public int base;
    public int altura;
    public int ladoA;
    public int ladoB;
    public int ladoC;

    public Triangulo(int base, int altura, int ladoA, int ladoB, int ladoC) {
        this.base = base;
        this.altura = altura;
        this.ladoA = ladoA;
        this.ladoB = ladoB;
        this.ladoC = ladoC;
    }


}
